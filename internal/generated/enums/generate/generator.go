package main

import (
	"fmt"
	"go/format"
	"sort"
	"strconv"
	"strings"
)

func generate(sites []enumSite) ([]byte, error) {
	var b strings.Builder

	b.WriteString("// Code generated by enums generator. DO NOT EDIT.\n\n")
	b.WriteString("package enums\n\n")

	seen := make(map[string]string, len(sites))
	for _, s := range sites {
		name := toExported(s.ID)
		if prev, ok := seen[name]; ok {
			return nil, fmt.Errorf("duplicate var %q: %s and %s", name, prev, s.SourcePath)
		}
		seen[name] = s.SourcePath
		decl, err := siteDecl(s)
		if err != nil {
			return nil, err
		}
		fmt.Fprintf(&b, "// Source: %s\nvar %s = %s\n\n", s.SourcePath, name, decl)
	}

	b.WriteString("// Table maps enum values by (OpenAPI schemaName, camelFieldName).\n")
	b.WriteString("var Table = map[string]map[string][]string{\n")

	lookup := buildLookup(sites)
	for _, schemaName := range sortedKeys(lookup) {
		fields := lookup[schemaName]
		fmt.Fprintf(&b, "\t%q: {\n", schemaName)
		for _, fieldName := range sortedKeys(fields) {
			fmt.Fprintf(&b, "\t\t%q: {%s},\n", fieldName, joinQuoted(fields[fieldName]))
		}
		b.WriteString("\t},\n")
	}

	b.WriteString("}\n")

	return format.Source([]byte(b.String()))
}

// buildLookup indexes string-typed schema enum sites by (schemaName, camelFieldName).
// Top-level enums (empty FieldPath) and parameter enums are excluded.
func buildLookup(sites []enumSite) map[string]map[string][]string {
	lookup := make(map[string]map[string][]string)
	for _, s := range sites {
		key := fieldKey(s.FieldPath)
		if s.Scope != scopeSchema || s.Type == "integer" || key == "" {
			continue
		}
		if lookup[s.SchemaName] == nil {
			lookup[s.SchemaName] = make(map[string][]string)
		}
		lookup[s.SchemaName][key] = s.Values
	}
	return lookup
}

// fieldKey derives the lookup key from a FieldPath.
// Array item paths (e.g. "roles.[]") are reduced to the property name ("roles").
func fieldKey(fieldPath string) string {
	key := strings.TrimSuffix(fieldPath, ".[]")
	key = strings.TrimSuffix(key, "[]")
	return key
}

func toExported(id string) string {
	if id == "" {
		return ""
	}
	return strings.ToUpper(id[:1]) + id[1:]
}

func siteDecl(s enumSite) (string, error) {
	switch s.Type {
	case "integer":
		for _, v := range s.Values {
			if _, err := strconv.ParseInt(v, 10, 64); err != nil {
				return "", fmt.Errorf("%s: integer enum has non-integer value %q", s.SourcePath, v)
			}
		}
		return fmt.Sprintf("[]int64{%s}", strings.Join(s.Values, ", ")), nil
	case "string", "":
		return fmt.Sprintf("[]string{%s}", joinQuoted(s.Values)), nil
	default:
		return "", fmt.Errorf("%s: unsupported enum type %q", s.SourcePath, s.Type)
	}
}

func joinQuoted(values []string) string {
	quoted := make([]string, len(values))
	for i, v := range values {
		quoted[i] = fmt.Sprintf("%q", v)
	}
	return strings.Join(quoted, ", ")
}

func sortedKeys[V any](m map[string]V) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}
