package main

import (
	"fmt"
	"go/format"
	"sort"
	"strings"
)

func generate(sites []enumSite) ([]byte, error) {
	return generateAll(sites, nil, nil, nil)
}

func generateAll(enumSites []enumSite, compSites []compositionSite, reqSites []requiredSite, constrSites []constraintSite) ([]byte, error) {
	var b strings.Builder

	b.WriteString("// Code generated by enums generator. DO NOT EDIT.\n\n")
	b.WriteString("package enums\n\n")

	// Helper functions for pointer creation used by constraintTable
	b.WriteString("func ptrFloat64(v float64) *float64 { return &v }\n")
	b.WriteString("func ptrInt64(v int64) *int64 { return &v }\n\n")

	b.WriteString("// EnumDef describes one enum-constrained property. Type is the OpenAPI\n")
	b.WriteString("// scalar type (\"string\" or \"integer\"). IsArray is true when the property\n")
	b.WriteString("// itself is an array; values constrain each element.\n")
	b.WriteString("type EnumDef struct {\n")
	b.WriteString("\tType    string\n")
	b.WriteString("\tValues  []string\n")
	b.WriteString("\tIsArray bool\n")
	b.WriteString("}\n\n")

	b.WriteString("var enumTable = map[string]map[string]EnumDef{\n")

	table := buildEnumTable(enumSites)
	for _, schemaName := range sortedKeys(table) {
		fmt.Fprintf(&b, "\t%q: {\n", schemaName)
		fields := table[schemaName]
		for _, fieldName := range sortedKeys(fields) {
			def := fields[fieldName]
			fmt.Fprintf(&b, "\t\t%q: {Type: %q, %sValues: []string{%s}},\n",
				fieldName, def.Type, isArrayLiteral(def.IsArray), joinQuoted(def.Values))
		}
		b.WriteString("\t},\n")
	}

	b.WriteString("}\n\n")

	b.WriteString("// CompositionDef describes a oneOf/anyOf/allOf composition site.\n")
	b.WriteString("// Kind is the OpenAPI composition keyword. Branches are the schema names\n")
	b.WriteString("// from the $ref targets in the composition.\n")
	b.WriteString("type CompositionDef struct {\n")
	b.WriteString("\tKind     string\n")
	b.WriteString("\tBranches []string\n")
	b.WriteString("}\n\n")

	b.WriteString("var compositionTable = map[string]map[string]CompositionDef{\n")

	compTable := buildCompositionTable(compSites)
	for _, schemaName := range sortedKeys(compTable) {
		fmt.Fprintf(&b, "\t%q: {\n", schemaName)
		fields := compTable[schemaName]
		for _, fieldName := range sortedKeys(fields) {
			def := fields[fieldName]
			fmt.Fprintf(&b, "\t\t%q: {Kind: %q, Branches: []string{%s}},\n",
				fieldName, def.Kind, joinQuoted(def.Branches))
		}
		b.WriteString("\t},\n")
	}

	b.WriteString("}\n\n")

	b.WriteString("// RequiredDef marks a field as required per the OpenAPI spec.\n")
	b.WriteString("type RequiredDef struct{}\n\n")

	b.WriteString("var requiredTable = map[string]map[string]RequiredDef{\n")

	reqTable := buildRequiredTable(reqSites)
	for _, schemaName := range sortedKeys(reqTable) {
		fmt.Fprintf(&b, "\t%q: {\n", schemaName)
		fields := reqTable[schemaName]
		for _, fieldName := range sortedKeys(fields) {
			fmt.Fprintf(&b, "\t\t%q: {},\n", fieldName)
		}
		b.WriteString("\t},\n")
	}

	b.WriteString("}\n\n")

	b.WriteString("// ConstraintDef describes min/max constraints for a property.\n")
	b.WriteString("// Pointer fields are nil when the constraint is not specified.\n")
	b.WriteString("type ConstraintDef struct {\n")
	b.WriteString("\tMinimum   *float64\n")
	b.WriteString("\tMaximum   *float64\n")
	b.WriteString("\tMinLength *int64\n")
	b.WriteString("\tMaxLength *int64\n")
	b.WriteString("\tMinItems  *int64\n")
	b.WriteString("\tMaxItems  *int64\n")
	b.WriteString("}\n\n")

	b.WriteString("var constraintTable = map[string]map[string]ConstraintDef{\n")

	constrTable := buildConstraintTable(constrSites)
	for _, schemaName := range sortedKeys(constrTable) {
		fmt.Fprintf(&b, "\t%q: {\n", schemaName)
		fields := constrTable[schemaName]
		for _, fieldName := range sortedKeys(fields) {
			def := fields[fieldName]
			fmt.Fprintf(&b, "\t\t%q: {%s},\n", fieldName, formatConstraintDef(def))
		}
		b.WriteString("\t},\n")
	}

	b.WriteString("}\n")

	return format.Source([]byte(b.String()))
}

// buildEnumTable indexes schema enum sites by (schemaName, fieldKey).
// Top-level enums (empty FieldPath) and parameter enums are excluded.
// Both string and integer enums are included; consumers parse integer
// values from the string representation.
func buildEnumTable(sites []enumSite) map[string]map[string]EnumDef {
	table := make(map[string]map[string]EnumDef)
	for _, s := range sites {
		if s.Scope != scopeSchema {
			continue
		}
		if s.Type != "string" && s.Type != "integer" {
			continue
		}
		isArray := strings.HasSuffix(s.FieldPath, "[]")
		key := strings.TrimSuffix(s.FieldPath, ".[]")
		key = strings.TrimSuffix(key, "[]")
		if key == "" {
			continue
		}
		if table[s.SchemaName] == nil {
			table[s.SchemaName] = make(map[string]EnumDef)
		}
		table[s.SchemaName][key] = EnumDef{
			Type:    s.Type,
			Values:  s.Values,
			IsArray: isArray,
		}
	}
	return table
}

// EnumDef mirrors the runtime type defined in the enums package. Used
// only for in-generator bookkeeping; the generated source emits map
// literals that target the enums.EnumDef type directly.
type EnumDef struct {
	Type    string
	Values  []string
	IsArray bool
}

// CompositionDef mirrors the runtime type for composition table entries.
type CompositionDef struct {
	Kind     string
	Branches []string
}

func buildCompositionTable(sites []compositionSite) map[string]map[string]CompositionDef {
	table := make(map[string]map[string]CompositionDef)
	for _, s := range sites {
		// Normalize key the same way as enumTable: strip trailing [] and .[]
		key := strings.TrimSuffix(s.FieldPath, ".[]")
		key = strings.TrimSuffix(key, "[]")
		if key == "" {
			continue
		}
		if table[s.SchemaName] == nil {
			table[s.SchemaName] = make(map[string]CompositionDef)
		}
		table[s.SchemaName][key] = CompositionDef{
			Kind:     string(s.Kind),
			Branches: s.Branches,
		}
	}
	return table
}

// RequiredDef mirrors the runtime type for required table entries.
type RequiredDef struct{}

func buildRequiredTable(sites []requiredSite) map[string]map[string]RequiredDef {
	table := make(map[string]map[string]RequiredDef)
	for _, s := range sites {
		// Normalize key the same way as enumTable: strip trailing [] and .[]
		key := strings.TrimSuffix(s.FieldPath, ".[]")
		key = strings.TrimSuffix(key, "[]")
		if key == "" {
			continue
		}
		if table[s.SchemaName] == nil {
			table[s.SchemaName] = make(map[string]RequiredDef)
		}
		table[s.SchemaName][key] = RequiredDef{}
	}
	return table
}

// ConstraintDef mirrors the runtime type for constraint table entries.
type ConstraintDef struct {
	Minimum   *float64
	Maximum   *float64
	MinLength *int64
	MaxLength *int64
	MinItems  *int64
	MaxItems  *int64
}

func buildConstraintTable(sites []constraintSite) map[string]map[string]ConstraintDef {
	table := make(map[string]map[string]ConstraintDef)
	for _, s := range sites {
		// Normalize key the same way as enumTable: strip trailing [] and .[]
		key := strings.TrimSuffix(s.FieldPath, ".[]")
		key = strings.TrimSuffix(key, "[]")
		if key == "" {
			continue
		}
		if table[s.SchemaName] == nil {
			table[s.SchemaName] = make(map[string]ConstraintDef)
		}
		table[s.SchemaName][key] = ConstraintDef{
			Minimum:   s.Minimum,
			Maximum:   s.Maximum,
			MinLength: s.MinLength,
			MaxLength: s.MaxLength,
			MinItems:  s.MinItems,
			MaxItems:  s.MaxItems,
		}
	}
	return table
}

func formatConstraintDef(def ConstraintDef) string {
	var parts []string
	if def.Minimum != nil {
		parts = append(parts, fmt.Sprintf("Minimum: ptrFloat64(%v)", *def.Minimum))
	}
	if def.Maximum != nil {
		parts = append(parts, fmt.Sprintf("Maximum: ptrFloat64(%v)", *def.Maximum))
	}
	if def.MinLength != nil {
		parts = append(parts, fmt.Sprintf("MinLength: ptrInt64(%d)", *def.MinLength))
	}
	if def.MaxLength != nil {
		parts = append(parts, fmt.Sprintf("MaxLength: ptrInt64(%d)", *def.MaxLength))
	}
	if def.MinItems != nil {
		parts = append(parts, fmt.Sprintf("MinItems: ptrInt64(%d)", *def.MinItems))
	}
	if def.MaxItems != nil {
		parts = append(parts, fmt.Sprintf("MaxItems: ptrInt64(%d)", *def.MaxItems))
	}
	return strings.Join(parts, ", ")
}

func isArrayLiteral(isArray bool) string {
	if isArray {
		return "IsArray: true, "
	}
	return ""
}

func joinQuoted(values []string) string {
	quoted := make([]string, len(values))
	for i, v := range values {
		quoted[i] = fmt.Sprintf("%q", v)
	}
	return strings.Join(quoted, ", ")
}

func sortedKeys[V any](m map[string]V) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}
